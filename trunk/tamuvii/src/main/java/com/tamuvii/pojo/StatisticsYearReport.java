package com.tamuvii.pojo;

public class StatisticsYearReport {
	private Integer year;
	private Integer numMovies;
	private Integer totMinutes;
	
	public Integer getYear() {
		return year;
	}
	public void setYear(Integer year) {
		this.year = year;
	}
	public Integer getNumMovies() {
		return numMovies;
	}
	public void setNumMovies(Integer numMovies) {
		this.numMovies = numMovies;
	}
	public Integer getTotMinutes() {
		return totMinutes;
	}
	public void setTotMinutes(Integer totMinutes) {
		this.totMinutes = totMinutes;
	}

}